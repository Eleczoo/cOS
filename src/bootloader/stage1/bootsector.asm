; GLOBAL DESCRIPTOR TABLE
; https://wiki.osdev.org/Global_Descriptor_Table
GDT_Start:
	GDT_null:
		dq 0	; First entry is null
	GDT_code:
		; base  = 0
		; limit = 0xfffff
		dw 0xffff	; limit bits 0-15
		dw 0		; base  bits 0-15
		db 0		; base  bits 16-23
		db 0x9A		; Access byte
		db 0xCF		; Flags + limit bits 16-19
		db 0		; base 	bits 24-31
	GDT_data:
		; base  = 0
		; limit = 0xfffff
		dw 0xffff	; limit bits 0-15
		dw 0		; base  bits 0-15
		db 0		; base  bits 16-23
		db 0x92		; Access byte
		db 0xCF		; Flags + limit bits 16-19
		db 0		; base 	bits 24-31
